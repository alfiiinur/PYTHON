###################### 5 STRING ################

#1. Buat kalimat menggunakan perintah string sederhana
name = "alfii nur "
print("name[2 : 9]: ", name[2:9])
print("name[0 : 9]: ", name[0:9])

#2. Pada kalimat "saya sedang belajar python" ubahlah menjadi huruf besar
kalimat = "saya sedang belajar python"
print(kalimat.upper())

#3. "AKU BENCI, TAPI SUKA" ubahlah menjadi huruf kecil
kalimat = "AKU BENCI, TAPI SUKA"
print(kalimat.lower())

#4. Kata "kamu" pada kalimat "Aku sayang kamu", ganti menjadi "dia"
kalimat = "Aku sayang kamu"
print(kalimat[:11] + "dia")
